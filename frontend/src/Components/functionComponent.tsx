export function nameToKor(name: string) {
  if (name === "enoki") {
    return "팽이버섯";
  } else if (name === "shitake") {
    return "표고버섯";
  } else if (name === "songi") {
    return "새송이버섯";
  } else if (name === "yellowegg") {
    return "노란달걀버섯";
  } else if (name === "woodear") {
    return "목이버섯";
  } else if (name === "neungi") {
    return "능이버섯";
  } else if (name === "noru") {
    return "노루궁뎅이버섯";
  } else if (name === "songe") {
    return "송이버섯";
  } else if (name === "youngji") {
    return "영지버섯";
  } else {
    return "버섯없음";
  }
}
